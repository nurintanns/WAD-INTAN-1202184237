    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cek = Product::findorfail($request->id);
        if($cek->stock >= $request->amount){
            $order = new Order();
            $order->buyer_name = $request->buyer_name;
            $order->buyer_contact = $request->buyer_contact;
            $order->amout = $request->amount;
            $order->product_id = $request->id;
            $order->save();

            $cek->stock = $cek->stock-$request->amount;
            $cek->save();
        }else{
            return redirect()->back()->with('error','Stock tidak tersedia.');
        }

        return redirect()->route('order.index');

    }

